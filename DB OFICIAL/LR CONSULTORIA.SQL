THEN SELECT 'EELS' AS 'MENSAJE';

ELSE
INSERT INTO EMAILS_DETALLE(IDDetalle,HoraRegistro,HoraEnvio)
VALUES(IN_IDD,now(),DATE_ADD(now(), INTERVAL 5 HOUR));
END IF;
END $$


CREATE PROCEDURE SP_GET_CC_DDC
(IN_DDC INT)
BEGIN
 SELECT C.EmailCliente,C.CC FROM DETALLE_CITACION AS DC
 JOIN NOTIF_INCONSISTENCIA AS N ON N.IDNotificacion = DC.IDNotificacion
  JOIN CLIENTES AS C ON N.IDCliente = C.IDCliente
 WHERE DC.IDDetalle = IN_DDC;
END$$


CREATE PROCEDURE SP_ADD_CC_DDC
(IN_DDC INT, IN_CC JSON)
BEGIN
DECLARE IN_CL INT;

 SELECT C.IDCliente FROM DETALLE_CITACION AS DC
 JOIN NOTIF_INCONSISTENCIA AS N ON N.IDNotificacion = DC.IDNotificacion
 JOIN CLIENTES AS C ON N.IDCliente = C.IDCliente
 WHERE DC.IDDetalle = IN_DDC INTO IN_CL;
 
IF (SELECT IFNULL(SUM(JSON_CONTAINS(CC, IN_CC)), 0) FROM CLIENTES WHERE IDCliente = IN_CL) = 0 THEN
UPDATE CLIENTES SET CC = IN_CC WHERE IDCliente = IN_CL;
END IF;
END$$


CREATE PROCEDURE SENDMAIL_DETALLE(IN SEARCH INT) BEGIN
SELECT 
	E.IDDetalle,
    C.NombreCliente,
    C.EmailCliente,
    JSON_EXTRACT(N.Notificacion, '$[*].NOTIFICACION') AS NOTIFICACION,
    D.FECHAVenci,
    D.DetallesCitacion AS INCONSISTENCIAS,
    D.CartasDetalles AS ARCHIVOS
FROM EMAILS_DETALLE AS E
JOIN DETALLE_CITACION AS D ON E.IDDetalle = D.IDDetalle
JOIN NOTIF_INCONSISTENCIA AS N ON D.IDNotificacion = N.IDNotificacion
JOIN CLIENTES AS C ON N.IDCliente = C.IDCliente
WHERE E.Estatus = "F" AND D.IDDetalle = SEARCH;
END $$



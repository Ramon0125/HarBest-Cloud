 WHERE N.IDNotificacion = IN_NTF INTO IN_CL;
 
IF (SELECT IFNULL(SUM(JSON_CONTAINS(CC, IN_CC)), 0) FROM CLIENTES WHERE IDCliente = IN_CL) = 0 THEN
UPDATE CLIENTES SET CC = IN_CC WHERE IDCliente = IN_CL;
END IF;
END$$


CREATE PROCEDURE SP_INSERTAR_DETALLE
(
IN INIDNOT INT,
IN INNOCAS BIGINT,
IN INFECHA DATE,
IN INDETALL JSON,
IN INCARTAS JSON,
IN CORAUD varchar(50),
IN NOMAUD varchar(50),
IN TELAUD varchar(20)
) BEGIN

DECLARE INFECVEN DATE;

SELECT DIASLABORABLES(INFECHA,20) INTO INFECVEN;

IF NOT EXISTS 
 (
  SELECT * FROM NOTIF_INCONSISTENCIA WHERE IDNotificacion = INIDNOT
 ) THEN SELECT 'EELS' AS MENSAJE;

ELSE 
 IF EXISTS 
 (
  SELECT * FROM DETALLE_CITACION WHERE 
  NOCaso = INNOCAS OR 
  IDNotificacion = INIDNOT OR 
  IFNULL((SELECT SUM(JSON_CONTAINS(JSON_EXTRACT(DetallesCitacion, '$[*].NOTIFICACION'), JSON_EXTRACT(INDETALL, '$[*].NOTIFICACION')))), 0) <> 0 
 ) THEN SELECT 'EDCE' AS MENSAJE;

ELSE 
	INSERT INTO DETALLE_CITACION
	(
	 IDNotificacion,
   NOCaso,
   Fecha,
   FechaVenci,
   DetallesCitacion,
   CartasDetalles,
   CorreoAuditor,
   NombreAuditor,
   TelAuditor
  ) VALUES(INIDNOT,INNOCAS,INFECHA,INFECVEN,INDETALL,INCARTAS,CORAUD,NOMAUD,TELAUD);  

IF ROW_COUNT() > 0 THEN
SELECT 'DIC' AS MENSAJE;
ELSE SELECT 'EID' AS MENSAJE; END IF;

END IF;
END IF;
END $$


CREATE PROCEDURE SP_INSERT_EMAIL_DDC
(
IN IN_NOCAS BIGINT
) BEGIN

DECLARE IN_IDD INT;

SELECT IDDetalle FROM DETALLE_CITACION WHERE NOCaso = IN_NOCAS INTO IN_IDD;

